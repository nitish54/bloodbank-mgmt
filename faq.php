                            <a href="#" onclick= "showdiv('q1');"><b>1. Why one is not allowed to donate blood before attaining the age of 18 years? </b></a>
                            <br />
                            <div id = "q1" name="q1" >
                                Physiologically there is no harm in donating blood in the early age. The reason is legal, 18 being the age of consent in our country.
                            </div>      
                            <br />
                            <a href="#" onclick= "showdiv('q2');"><b>2. Blood is collected from a vein. Naturally, it is rich in carbon dioxide content. What purpose does it serve? Why is blood not collected from an artery to get blood rich in oxygen content?</b></a>
                            <br />
                            <div id = "q2" name="q2" onclick= "showdiv('q2');">
                                Though the blood collected from a vein is rich in carbon dioxide, it is transfused in the vein of the recipient and is automatically oxygenate in course of normal circulation and therefore serves the required purpose. 
                                <br />Blood is not collected from artery for the following reasons: Veins remain in the upper surface of the body muscle and can be easily identified while arteries remain in the sub-surfaces and therefore cannot be easily identified. Blood pressure in vein is low compared to that in artery. Puncturing of artery causes bleeding at higher pressure and velocity leading to trouble in stopping the bleeding after donation which is not a problem in case of vein. 
                            </div>      

                            <br />
                            <a href="#" onclick= "showdiv('q3');"><b>
                                    3. Why is my finger pricked before donation?
                                </b></a>
                            <br />
                            <div id = "q3" name="q3" onclick= "showdiv('q2');">
                                To estimate "haemoglobin" content in blood. This is the component of the red cells which transport oxygen in the human body to all the organs and tissues. If the level is low, it is not wise to give blood.
                            </div>    

                            <br />
                            <a href="#" onclick= "showdiv('q4');"><b>
                                    4. What physical tests are performed before blood donation?
                                </b></a>
                            <br />
                            <div id = "q4" name="q4" onclick= "showdiv('q4');">
                                1. Weighing (not less than 45 kg)<br />
                                2. Hb estimation (12.5gm/100ml by copper sulphate method) <br />
                                3. Measurement of blood pressure (systolic 160/100 mmHg; diastolic 90/60 mmHg)<br />
                                4. Checking heart beats (60 - 100 times per minute), condition of liver, lung and spleen<br />
                                5. Body temperature (37<sup>o</sup>C)      
                            </div>      

                            <br />
                            <a href="#" onclick= "showdiv('q5');"><b>
                                    5. Why blood is not collected from a person having a haemoglobin content of less than 12.5 gm/l00ml?
                                </b></a>
                            <br />
                            <div id = "q5" name="q5" onclick= "showdiv('q5');">
                                Hemoglobin in the blood carries oxygen from the respiratory organs (lungs ) to the rest of the body (i.e. the tissues) where it releases the oxygen to burn nutrients to provide energy to the organism, and collects the resultant carbon dioxide to bring it back to the respiratory organs to be dispensed from the organism. Hence blood with less Hb content than the prescribed (12.5gm/100ml) does not help the patient much in carrying oxygen to cells.
                            </div>      


                            <br />
                            <a href="#" onclick= "showdiv('q6');"><b>
                                    6. I've been deferred before. Should I try again?
                                </b></a>
                            <br />
                            <div id = "q6" name="q6" onclick= "showdiv('q6');">
                                Yes, you can, most deferrals are temporary-not permanent. 
                            </div>  

                            <br />
                            <a href="#" onclick= "showdiv('q7');"><b>
                                    7. Is blood donation very painful?
                                </b></a>
                            <br />
                            <div id = "q7" name="q7" onclick= "showdiv('q7');">
                                No. Not more than a prick of an injection needle.
                            </div>      


                            <br />
                            <a href="#" onclick= "showdiv('q8');"><b>
                                    8. Does it take long time to donate blood?
                                </b></a>
                            <br />
                            <div id = "q8" name="q8" onclick= "showdiv('q8');">
                                It should take up not more than 20 minutes of time including time for rest and taking refreshment.
                            </div>       



                            <br />
                            <a href="#" onclick= "showdiv('q9');"><b>
                                    9. How much blood do you take?
                                </b></a>
                            <br />
                            <div id = "q9" name="q9" onclick= "showdiv('q9');">
